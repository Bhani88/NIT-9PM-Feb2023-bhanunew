package JavaConcepts;

public class wrapperClasses {
    public static void main(String[] args){
        //Wrapper Classes are used to convert Primitive Data types to Objects.
        //int - Integer
        //float - Float
        //byte - Byte
        //short - Short
        //double - Double
        //char - Character
        //boolean - Boolean

        int i = 5; //Primitive Data type
        Integer a = 4; //Wrapper
    }
}
